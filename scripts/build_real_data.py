def parse_data():
    """Parse the raw data into structured records."""
    records = []
    seen_draws = set()

    for line in RAW_DATA.strip().split('\n'):
        line = line.strip()
        if not line:
            continue

        parts = line.split('|')
        if len(parts) != 4:
            continue

        draw_num = int(parts[0].strip())
        date_str = parts[1].strip()
        numbers_str = parts[2].strip()
        additional_str = parts[3].strip()

        # Skip duplicates
        if draw_num in seen_draws:
            continue
        seen_draws.add(draw_num)

        # Parse main numbers
        nums = [int(n.strip()) for n in numbers_str.split(',')]
        if len(nums) != 6:
            print(f"Warning: Draw {draw_num} has {len(nums)} numbers, skipping")
            continue

        # Parse additional number (take first number if garbled)
        try:
            additional = int(additional_str.split(',')[0].strip())
        except ValueError:
            print(f"Warning: Draw {draw_num} has bad additional number '{additional_str}', skipping")
            continue

        # Validate all numbers are 1-49
        all_nums = nums + [additional]
        if not all(1 <= n <= 49 for n in all_nums):
            print(f"Warning: Draw {draw_num} has out-of-range numbers, skipping")
            continue

        # Parse date
        try:
            dt = datetime.strptime(date_str, '%Y-%m-%d')
            day_of_week = dt.strftime('%A')
        except ValueError:
            print(f"Warning: Draw {draw_num} has bad date '{date_str}', skipping")
            continue

        records.append({
            'draw_number': draw_num,
            'date': date_str,
            'day_of_week': day_of_week,
            'num1': nums[0],
            'num2': nums[1],
            'num3': nums[2],
            'num4': nums[3],
            'num5': nums[4],
            'num6': nums[5],
            'additional_number': additional,
            'group1_prize': 0,
            'group1_winners': 0,
            'is_synthetic': False
        })

    # Sort by date ascending
    records.sort(key=lambda r: r['date'])
    return records

def write_csv(records, output_path):
    """Write records to CSV."""
    fieldnames = [
        'draw_number', 'date', 'day_of_week',
        'num1', 'num2', 'num3', 'num4', 'num5', 'num6',
        'additional_number', 'group1_prize', 'group1_winners', 'is_synthetic'
    ]

    with open(output_path, 'w', newline='', encoding='utf-8') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(records)

def main():
    records = parse_data()

    # Stats
    print(f"Total unique draws parsed: {len(records)}")
    print(f"Date range: {records[0]['date']} to {records[-1]['date']}")

    from datetime import datetime as dt
    first = dt.strptime(records[0]['date'], '%Y-%m-%d')
    last = dt.strptime(records[-1]['date'], '%Y-%m-%d')
    years = (last - first).days / 365.25
    print(f"Span: {years:.1f} years")
    print(f"All data is REAL (is_synthetic=False)")

    # Validate
    errors = 0
    for r in records:
        nums = [r['num1'], r['num2'], r['num3'], r['num4'], r['num5'], r['num6']]
        if len(set(nums)) != 6:
            print(f"ERROR: Duplicate numbers in draw {r['draw_number']}: {nums}")
            errors += 1
        if sorted(nums) != nums:
            print(f"WARNING: Numbers not sorted in draw {r['draw_number']}: {nums}")

    if errors == 0:
        print("Validation: All draws have 6 unique numbers")

    # Write
    output_path = os.path.join(os.path.dirname(os.path.dirname(__file__)), 'data', 'toto_results.csv')
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    write_csv(records, output_path)
    print(f"\nWritten to: {output_path}")

